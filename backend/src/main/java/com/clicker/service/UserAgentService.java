    public String getRandom() {
        return userAgents.get(random.nextInt(userAgents.size()));
    }

    public String getForDeviceProfile(String device, String browser) {
        List<String> pool = switch (device.toLowerCase()) {
            case "mobile", "phone", "smartphone" -> switch (browser.toLowerCase()) {
                case "safari" -> MOBILE_IOS;
                default -> MOBILE_ANDROID;
            };
            case "tablet", "ipad" -> MOBILE_IOS;
            default -> switch (browser.toLowerCase()) {
                case "firefox", "ff" -> DESKTOP_FIREFOX;
                case "safari" -> DESKTOP_SAFARI;
                default -> DESKTOP_CHROME;
            };
        };
        return pool.get(random.nextInt(pool.size()));
    }

    public List<String> getAll() {
        return List.copyOf(userAgents);
    }
